        var random = Random.Range(1, 10);
        return random switch
        {
            1 => "John",
            2 => "Bob",
            3 => "Jef",
            4 => "Fries",
            5 => "Sara",
            6 => "Louis",
            7 => "Cedric",
            8 => "Alexander",
            9 => "Filou",
            _ => throw new System.NotImplementedException(),
        };
    }

    public static byte GetRandomByte()
    {
        System.Random random = new();
        byte newByte = (byte)random.Next(0, 255);
        return newByte;
    }
